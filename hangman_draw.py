def print_hangman(stage):
    margin = 10

    stages = [
        [
            " -----",
            " |   |",
            " |   O",
            " |  /|\\",
            " |   |",
            " |  / \\",
            " |   "
        ],  # Right Leg
        [
            " -----",
            " |   |",
            " |   O",
            " |  /|\\",
            " |   |",
            " |  /",
            " |   "
        ],  # Left Leg
        [
            " -----",
            " |   |",
            " |   O",
            " |  /|\\",
            " |   |",
            " |   ",
            " |   "
        ],  # Right Arm
        [
            " -----",
            " |   |",
            " |   O",
            " |  /|",
            " |   |",
            " |   ",
            " |   "
        ],  # Left Arm
        [
            " -----",
            " |   |",
            " |   O",
            " |   |",
            " |   |",
            " |   ",
            " |   "
        ],  # Body
        [
            " -----",
            " |   |",
            " |   O",
            " |   ",
            " |   ",
            " |   ",
            " |   "
        ],  # Head
        [
            " -----",
            " |   |",
            " |   ",
            " |   ",
            " |   ",
            " |   ",
            " |   "
        ]  # Stand and Rope
    ]

    for line in stages[stage]:
        print(" " * margin + line)
